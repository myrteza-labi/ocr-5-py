def sum(a, b):
    """
    Calcule la somme de deux nombres.

    Parameters:
    a (int or float): Le premier nombre à additionner.
    b (int or float): Le deuxième nombre à additionner.

    Returns:
    int or float: La somme de `a` et `b`.
    """
    return a + b
 
def subtraction(a, b):
    """
    Calcule la différence entre deux nombres.

    Parameters:
    a (int or float): Le nombre à partir duquel soustraire.
    b (int or float): Le nombre à soustraire.

    Returns:
    int or float: La différence entre `a` et `b`.
    """
    return a - b
